function t(userOrContext, key, defaultText = '', variables = {}) {
    let langCode = 'fr';
    if (userOrContext) {
        if (typeof userOrContext === 'string') {
            langCode = userOrContext;
        } else if (userOrContext.language_code) {
            langCode = userOrContext.language_code;
        } else if (userOrContext.from && userOrContext.from.language_code) {
            langCode = userOrContext.from.language_code;
        } else if (userOrContext.data && userOrContext.data.language) {
            langCode = userOrContext.data.language;
        } else if (userOrContext.language) {
            langCode = userOrContext.language;
        }
    }
    
    // Ensure we only use supported languages
    if (!translations[langCode]) {
        langCode = 'fr'; // default fallback
    }

    let text = translations[langCode][key];
    if (!text) {
        text = translations['fr'][key] || defaultText || key;
    }

    for (const [k, v] of Object.entries(variables)) {
        text = text.replace(new RegExp(`{${k}}`, 'g'), v);
    }

    return text;
}

